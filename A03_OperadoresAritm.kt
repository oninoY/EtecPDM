fun main(){

    // aula 03: Operadores Aritmeticos ( +  -  *  /  % )
    var idade = 20


    // idade = idade + 1 (soma)
    idade += 1

    // idade = idade - 5 (subtração)
    idade -= 5

    //idade = idade * 2 (multiplicação)
    idade *= 2

    //idade = idade / 2 (divisão)
    idade /= 2

    //idade = idade % 3 (resto da divisão)
    idade %= 3

    println("Quantos anos tem este edifício? Acho que $idade")
}